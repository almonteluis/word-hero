const _dictCache = {};

function getDictionary(lang = "en") {
  if (_dictCache[lang]) return _dictCache[lang];

  const { ALL_WORDS } = getWordBank(lang);
  const base = new Set(ALL_WORDS.map((w) => w.toLowerCase()));

  if (lang === "en") {
    SUPPLEMENTAL_EN.forEach((w) => base.add(w.toLowerCase()));
  } else if (lang === "es") {
    SUPPLEMENTAL_ES.forEach((w) => base.add(w.toLowerCase()));
  }

  _dictCache[lang] = base;
  return base;
}

function isValidWord(word, lang = "en") {
  const dict = getDictionary(lang);
  return dict.has(word.toLowerCase());
}

function isSightWord(word, lang = "en") {
  const { ALL_WORDS } = getWordBank(lang);
  return ALL_WORDS.map((w) => w.toLowerCase()).includes(word.toLowerCase());
}

export { isValidWord, isSightWord, getDictionary, SUPPLEMENTAL_EN, SUPPLEMENTAL_ES };
